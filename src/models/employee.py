class Employee:
    def __init__(self, emp_id, emp_available, emp_type, emp_level, emp_status, emp_assigned):
        self.emp_id = emp_id
        self.emp_available = emp_available
        self.emp_type = emp_type
        self.emp_level = emp_level
        self.emp_status = emp_status
        self.emp_assigned = emp_assigned